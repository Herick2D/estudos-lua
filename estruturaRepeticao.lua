-- Enquanto for verdade faça
local num = 1

while num <= 10 do -- condição será verdadeira até a décida interação
    print(num)
    num = num + 1
end

print("\n")

-- Até ser verdadeiro faça
local repNum = 1
repeat
    print(repNum)
    repNum = repNum + 1
until repNum > 10 -- condição será falsa até a décima interação